function Saudacao() {
    $hr = date("H");
    if ($hr >= 12 && $hr < 18) {
        $Saudacao = "Boa tarde!";
    } else if ($hr >= 0 && $hr < 12) {
        $Saudacao = "Bom dia!";
    } else {
        $Saudacao = "Boa noite!";
    }
    return $Saudacao;
}

